const contract_address = "0x468840fCAa037b5DdEfCe8474F032ee87Ac7AacA";

let web3 = new Web3("http://127.0.0.1:7545");

async function main() {
  const accs = await web3.eth.getAccounts();
  console.log(accs);
  const contract = new web3.eth.Contract(abi, contract_address);

  const receiver = accs[1];

  const amount = web3.utils.toWei("10", "ether");
  console.log('Amount: ',amount);

  const mintTx = await contract.methods.mint(receiver, amount).send({from: accs[0]});
  console.log('Minted Successfully',mintTx.transactionHash);
  const transferTx = await contract.methods.transfer(receiver, amount).send({from: accs[0]});
  console.log('Transferred Successfully',transferTx.transactionHash);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
